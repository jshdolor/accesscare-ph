          <h4>What is Medical Auditing?</h4>
          <p>Quality health care is based on accurate and complete clinical documentation in the medical record. The best way to improve your clinical documentation and the livelihood of your health care organization is through medical record audits. They are necessary to determine areas that require improvements and corrections.</p>
          <p>The goals of an audit are to provide efficient and better delivery of care and to improve the financial health of your medical provider. Medical record audits specifically target and evaluate procedural and diagnosis code selection as determined by physician documentation. Once areas of weakness are revealed through an audit, you can present the audit findings and identify opportunities for training in your health care organization.</p>

          <h4>What Is Medical Auditing and Why Perform One?</h4>
          <p>Medical auditing entails conducting internal or external reviews of coding accuracy, policies, and procedures to ensure you are running an efficient and hopefully liability-free operation.</p>

          <p>There are many reasons to perform medical audits:</p>
          <ol>
            <li>To determine outliers before large payers find them in their claims software and request an internal audit be done.</li>
            <li>To protect against fraudulent claims and billing activity</li>
            <li>To reveal whether there is variation from national averages due to inappropriate coding, insufficient documentation, or lost revenue.</li>
            <li>To help identify and correct problem areas before insurance or government payers challenge inappropriate coding</li>
            <li>To help prevent governmental investigational auditors like recovery audit contractors (RACs) or zone program integrity contractors (ZPICs) from knocking at your door</li>
            <li>To remedy undercoding, bad unbundling habits, and code overuse and to bill appropriately for documented procedures</li>
            <li>To identify reimbursement deficiencies and opportunities for appropriate reimbursement.</li>
            <li>To stop the use of outdated or incorrect codes for procedures</li>
            <li>To verify ICD-10-CM and electronic health record (EHR) meaningful use readines</li>
          </ol>
          <p>
            The importance of your practice having someone who is certified in medical auditing is becoming greater and greater, 
            as quality healthcare is dependant on accurate and complete medical record documentation and coding.
          </p>
          <p>
            A certified medical auditor will be able to create an effective compliance program that will contain an annual review of coding and documentation 
            in order to minimize risk and increase earning potential. Through an internal audit, they will be able to hone in on areas of weakness where more 
            training and education could be beneficial to a practice in order to increase efficiency and overall revenue. Additionally, in the case that a payor 
            is requesting the audit, the certified auditor will know what to do and how to handle these important situations. 
        </p>
